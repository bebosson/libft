#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	int		len_s1;
	size_t	i;

	i = 0;
	len_s1 = 0;
	while (s1[len_s1])
		len_s1++;
	while (i < n && s2[i])
	{
		s1[len_s1 + i] = s2[i];
		i++;
	}
	s1[len_s1 + i] = '\0';
	return (s1);
}
